//package Session-03->(Basic Programs based on Operators & Formula's);

public class S2_1_Operators_in_java {
      public static void main(String[] args) {
        int a=5, b=2, c;
        c=a+b;

        System.out.println(a+"+"+b+"="+c);

        c=a-b;
        System.out.println(a+"-"+b+"="+c);

        c=a*b;
        System.out.println(a+"*"+b+"="+c);

        c=a/b;
        System.out.println(a+"/"+b+"="+c);

        c=a%b;
        System.out.println(a+"%"+b+"="+c);
      }
}
